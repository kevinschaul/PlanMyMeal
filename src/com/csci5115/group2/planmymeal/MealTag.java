package com.csci5115.group2.planmymeal;

public class MealTag
{
	private long id;
	private long mealId;
	private long tagId;
	
	public long getId()
	{
		return id;
	}
	public void setId(long id)
	{
		this.id = id;
	}
	public long getMealId()
	{
		return mealId;
	}
	public void setMealId(long mealId)
	{
		this.mealId = mealId;
	}
	public long getTagId()
	{
		return tagId;
	}
	public void setTagId(long tagId)
	{
		this.tagId = tagId;
	}

}
